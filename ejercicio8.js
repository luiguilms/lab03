let a =3,b=2;
let z=a+b;
//Incremento
//Pre-incremento (el operador ++ antes de la variable)
z=++a;
console.log(a);
console.log(z);

//Post incremento(el operador ++ despues de la variable)
z=b++;
console.log(b);
console.log(z);
//decremento
//predecremento
z=--a;
console.log(a);
console.log(z);

//Post decremento
z=b--;
console.log(b);
console.log(z);